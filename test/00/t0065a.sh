#!/bin/sh
#
prog="swTree_interp"
tmp=/tmp/$$
here=`pwd`
bin=$here/bin
if [ $? -ne 0 ]; then echo "Failed pwd"; exit 1; fi


fail()
{
        echo FAILED $prog 1>&2
        cd $here
        rm -rf $tmp
        exit 1
}


pass()
{
        echo PASSED $prog
        cd $here
        rm -rf $tmp
        exit 0
}

trap "fail" 1 2 3 15
mkdir $tmp
if [ $? -ne 0 ]; then echo "Failed mkdir"; exit 1; fi
cd $tmp
if [ $? -ne 0 ]; then echo "Failed cd"; fail; fi

#
# the input should look like this
#
cat > test.in << 'EOF'
k=10000;

# Random insert followed by random remove
"Random insert followed by random remove";
l=create();
for (x=0;x<k;x=x+1;)
{
  y=rand(k);
  insert(l,y);
}
for (x=0;x<k;x=x+1;)
{
  y=rand(k);
  p=find(l,y);
  if (p != 0)
  {
    remove(l,p);
    p=find(l,y);
    if (p != 0)
    {
      "ERROR remove failed!"; x=k;
    }
    else
    {
      "Removed"; print y;
    }
    "Check"; y=check(l); print y;
    if (y != 1)
    {
    "ERROR check failed!"; x=k;
    }
    "Size"; y=size(l); print y;
    "Depth"; y=depth(l); print y;
  }
}
destroy(l);


# In-order insert followed by in-order remove
"In-order insert followed by in-order remove";
l=create();
for (x=0;x<k;x=x+1;)
{
  insert(l,x);
}
for (x=0;x<k;x=x+1;)
{
  p=find(l,x);
  remove(l,p);
  p=find(l,x);
  if (p != 0)
  {
    "ERROR remove failed!"; x=k;
  }
  "Check"; y=check(l); print y;
  if (y != 1)
  {
    "ERROR check failed!"; x=k;
  }
  "Size"; y=size(l); print y;
  "Depth"; y=depth(l); print y;
}
destroy(l);

EOF
if [ $? -ne 0 ]; then echo "Failed input cat"; fail; fi

