if [ $? -ne 0 ]; then echo "Failed cd"; fail; fi

#
# the input should look like this
#
cat > test.in << 'EOF'
k=10000;

# Random insert followed by random remove
"Random insert followed by random remove";
l=create();
for (x=0;x<k;x=x+1;)
{
  y=rand(k);
  insert(l,y);
}
for (x=0;x<k;x=x+1;)
{
  y=rand(k);
  p=find(l,y);
  if (p != 0)
  {
    remove(l,p);
    p=find(l,y);
    if (p != 0)
    {
      "ERROR remove failed!"; x=k;
    }
    else
    {
      "Removed"; print y;
    }
    "Check"; y=check(l); print y;
    if (y != 1)
    {
    "ERROR check failed!"; x=k;
    }
    "Size"; y=size(l); print y;
    "Depth"; y=depth(l); print y;
  }
}
destroy(l);


# In-order insert followed by in-order remove
"In-order insert followed by in-order remove";
l=create();
for (x=0;x<k;x=x+1;)
{
  insert(l,x);
}
for (x=0;x<k;x=x+1;)
{
  p=find(l,x);
  remove(l,p);
  p=find(l,x);
  if (p != 0)
  {
    "ERROR remove failed!"; x=k;
  }
  "Check"; y=check(l); print y;
  if (y != 1)
  {
    "ERROR check failed!"; x=k;
  }
  "Size"; y=size(l); print y;
  "Depth"; y=depth(l); print y;
}
destroy(l);

EOF
if [ $? -ne 0 ]; then echo "Failed input cat"; fail; fi

